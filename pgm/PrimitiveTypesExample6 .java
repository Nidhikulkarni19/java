class PrimitiveTypesExample6 {
    public static void main(String[] args) {
        
        boolean isFireCold = false;
		System.out.println("boolean isFireCold: " + isFireCold);
        byte numberOfFloors = 15;
		System.out.println("byte numberOfFloors: " + numberOfFloors);
        short weeklyHoursWorked = 40;
		System.out.println("short weeklyHoursWorked: " + weeklyHoursWorked);
        int yearOfBirth = 1980;
		System.out.println("int yearOfBirth: " + yearOfBirth);
        long distanceToNearestStarInKm = 40200000000000L;
        System.out.println("long distanceToNearestStarInKm: " + distanceToNearestStarInKm);
        float discountRate = 12.5f;
        System.out.println("float discountRate: " + discountRate);
        double speedOfSound = 343.2d;
		System.out.println("double speedOfSound: " + speedOfSound);
        char genderInitial = 'M';
        System.out.println("char genderInitial: " + genderInitial);
    }
}
