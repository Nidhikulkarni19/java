class PrimitiveTypes {
    public static void main(String[] args) {
		byte smallNumber = 25;
		System.out.println("byte smallNumber: " + smallNumber);
		short shortNumber = 1500;
		System.out.println("short shortNumber: " + shortNumber);
		int age = 30;
		System.out.println("int age: " + age);
		long distanceToMarsInKm = 225000000L;
		System.out.println("long distanceToMarsInKm: " + distanceToMarsInKm);
		float priceOfCoffee = 3.99f;
		System.out.println("float priceOfCoffee: " + priceOfCoffee);
		double bankBalance = 1000.75;
		System.out.println("double bankBalance: " + bankBalance);
		boolean isJavaFun = true;
        System.out.println("boolean isJavaFun: " + isJavaFun);
		char currencySymbol = '$';
		System.out.println("char currencySymbol: " + currencySymbol);
   
    }
}
