class PrimitiveTypesExample8 {
    public static void main(String[] args) {
        
        byte ageOfDog = 7;
		System.out.println("byte ageOfDog: " + ageOfDog);
        short heightInCm = 175;
		System.out.println("short heightInCm: " + heightInCm);
        int yearOfInvention = 1876;
		System.out.println("int yearOfInvention: " + yearOfInvention);
        long nationalDebt = 28000000000000L;
		System.out.println("long nationalDebt: " + nationalDebt);       
        float goldenRatio = 1.618f;
		System.out.println("float goldenRatio: " + goldenRatio);        
        double avogadroNumber = 6.02214076e23d;
		System.out.println("double avogadroNumber: " + avogadroNumber);
        char gradeLetter = 'A';
		System.out.println("char gradeLetter: " + gradeLetter);		
        boolean isWaterDry = false;
        System.out.println("boolean isWaterDry: " + isWaterDry);
                
    }
}
