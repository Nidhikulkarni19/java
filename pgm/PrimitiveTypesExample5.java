class PrimitiveTypesExample5 {
    public static void main(String[] args) {
        
        byte levelOfBrightness = 127;
		System.out.println("byte levelOfBrightness: " + levelOfBrightness);
        short numberOfBooks = 32000;
		System.out.println("short numberOfBooks: " + numberOfBooks);
        int numberOfEmployees = 500;
		System.out.println("int numberOfEmployees: " + numberOfEmployees);
        long distanceToSunInKm = 149600000L;
        System.out.println("long distanceToSunInKm: " + distanceToSunInKm);
        float taxRate = 5.75f;
        System.out.println("float taxRate: " + taxRate);
        double heightOfMountEverest = 8848.86d;
        System.out.println("double heightOfMountEverest: " + heightOfMountEverest);
        char languageCode = 'E';
		System.out.println("char languageCode: " + languageCode);
        boolean isHomeworkExciting = false;
        System.out.println("boolean isHomeworkExciting: " + isHomeworkExciting);
      
    }
}
